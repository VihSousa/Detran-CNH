    private void btentrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btentrarActionPerformed
        ArrayList<Pessoa> lista2 = AdicionarPessoas.getLista();

        int cpf = Integer.parseInt(txtcpf.getText());
        int senha = Integer.parseInt(txtsenha.getText());

        boolean encontrou = false;

        for (Pessoa p : lista2) {
            int pCpf = p.getCpf();
            int pSenha = p.getSenha();

            if (cpf == pCpf && senha == pSenha) {
                encontrou = true;
                new BemVindoo().setVisible(true);
                this.dispose();
                break;
            }
        }

        if (!encontrou) {
            JOptionPane.showMessageDialog(null, "CPF ou senha incorreto.");
        }
    }//GEN-LAST:event_btentrarActionPerformed

